package com.android.p2pflowernet.project.view.activity;

import android.view.KeyEvent;

import com.android.p2pflowernet.project.mvp.KActivity;
import com.android.p2pflowernet.project.mvp.KFragment;
import com.android.p2pflowernet.project.view.fragments.authentication.result.AuthenticationResultFragment;

/**
 * Created by zhangkun on 2018/3/12.
 */

public class AuthenticationResultActivity extends KActivity {
    @Override
    protected KFragment getFirstFragment() {
        return AuthenticationResultFragment.newIntence();
    }

    @Override
    public boolean onKeyDown(int keyCode, KeyEvent event) {
        if (keyCode == KeyEvent.KEYCODE_BACK) {
            removeFragment();
            return true;
        }
        return super.onKeyDown(keyCode, event);
    }
}
